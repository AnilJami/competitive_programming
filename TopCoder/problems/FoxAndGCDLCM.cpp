long long FoxAndGCDLCM::get(long long G, long long L) {
    if (L % G != 0) return -1;
    long long ans = (1LL << 62);
    for (long long d = 1; d * d <= L; ++d) {
        if (L % d != 0) continue;
        long long a = d * G;
        long long b = (L / d);
        if (__gcd(a, b) == G and a + b < ans) ans = a + b;
    }
    return ans;
}




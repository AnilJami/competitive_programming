const double EPS = 1e-9;
int cmp(double x, double y = 0, double tol = EPS) {
    return (x <= y + tol) ? (x + tol < y) ? -1 : 0 : 1;
}

const int MAXN = 405;
int mat[MAXN][MAXN];

vector<int> A[2 * MAXN], B[2 * MAXN];

int main(){
    int n;
    scanf("%d", &n);
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            scanf("%d", &mat[i][j]);
        }
    }
    int ans = 0;
    for (int i = 0; i < n; ++i) {
        for (int j = 0; j < n; ++j) {
            for (int delta = 0; i + delta < n and j + delta < n; ++delta) {
                int option = 0;
                for (int k = 0; k <= delta; ++k) {
                    option += mat[i+k][j+k];
                    option -= mat[i+k][j+delta-k];
                }
                ans = max(option, ans);
            }
        }
    }
    cout << ans << endl;
    return 0;
}